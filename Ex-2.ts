// Exercise 2: Write a program that calculates the volume of a cube.
// In a cube, all the sides have same length therefore the volume of cube is (V= L^3) L*L*L.
function volumeOfCube(length:number) {
    console.log(`The value of length=${length} \nVolume of cube=`,length*length*length);
}
volumeOfCube(5);
